#!/usr/bin/env python3
"""
This module contains a function 'add' that
returns the sum of two float numbers.
"""


def add(a: float, b: float) -> float:
    """
    Add two float numbers and return their sum.

    :param a: The first float number
    :param b: The second float number
    :return: The sum of the two float numbers
    """
    return a + b
